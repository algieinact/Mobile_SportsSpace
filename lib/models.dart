class Community {
  final String id;
  final String name;
  final String image;
  final int members;
  final String sport;
  final String nextEvent;
  final String description;

  Community({
    required this.id,
    required this.name,
    required this.image,
    required this.members,
    required this.sport,
    required this.nextEvent,
    required this.description,
  });
}
